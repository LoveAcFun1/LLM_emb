class CustomDropout(torch.nn.Module):
    def __init__(self, p=0.5, q=0.5):
        super(CustomDropout, self).__init__()
        self.p = p
        self.q = q

    def forward(self, input_tensor):
        if self.training:
            mask = torch.bernoulli(torch.full_like(input_tensor, 1 - self.p))
            mask = torch.where(mask < 1, torch.tensor(self.q, dtype=input_tensor.dtype).to("cuda"), mask)
            delta = self.q * self.p
            output_tensor = (input_tensor * mask - delta) / (1 - self.p)
        else:
            output_tensor = input_tensor
        return output_tensor

